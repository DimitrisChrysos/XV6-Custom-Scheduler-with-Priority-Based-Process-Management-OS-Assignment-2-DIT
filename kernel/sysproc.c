uint64
sys_exit(void)
{
  int n;
  argint(0, &n);
  exit(n);
  return 0;  // not reached
}

uint64
sys_getpid(void)
{
  return myproc()->pid;
}

uint64
sys_fork(void)
{
  return fork();
}

uint64
sys_wait(void)
{
  uint64 p;
  argaddr(0, &p);
  return wait(p);
}

uint64
sys_sbrk(void)
{
  uint64 addr;
  int n;

  argint(0, &n);
  addr = myproc()->sz;
  if(growproc(n) < 0)
    return -1;
  return addr;
}

uint64
sys_sleep(void)
{
  int n;
  uint ticks0;

  argint(0, &n);
  acquire(&tickslock);
  ticks0 = ticks;
  while(ticks - ticks0 < n){
    if(killed(myproc())){
      release(&tickslock);
      return -1;
    }
    sleep(&ticks, &tickslock);
  }
  release(&tickslock);
  return 0;
}

uint64
sys_kill(void)
{
  int pid;

  argint(0, &pid);
  return kill(pid);
}

// return how many clock tick interrupts have occurred
// since start.
uint64
sys_uptime(void)
{
  uint xticks;

  acquire(&tickslock);
  xticks = ticks;
  release(&tickslock);
  return xticks;
}

// return the amount of free memory (in bytes)
uint64
sys_freemem(void)
{
  return calc_freemem();
}

// return 0, if successful priority set and -1, if not
uint64
sys_setpriority(void)
{
  int num;
  argint(0, &num);
  return set_priority(num);
}

// return 0, if successful info copy and -1, if not
uint64
sys_getpinfo(void)
{
  struct pstat info;
  argaddr(0, (uint64*)&info);
  get_pinfo(&info);
  struct proc *p = myproc();
  uint64 info_addr;
  argaddr(0, &info_addr);
  return copyout(p->pagetable, info_addr, (char*)&info, sizeof(info));
}